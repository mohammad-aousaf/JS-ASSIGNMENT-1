// Declare a variable called “temperature” and assign it a value of 72. Convert the temperature to Celsius and store the 
// result in a new variable called “celsius”. Display the value of celsius to the console

// Formula to convert the temprature from Fahrenheit to Celcius:
// C = (°F - 32) × 5 / 9

let tempInFahrenheit = 72;
let tempInCelcius = (tempInFahrenheit - 32) * (5 / 9);

console.log(`Converted value in Celcius, ${tempInCelcius}`);